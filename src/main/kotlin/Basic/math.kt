fun main(args: Array<String>) {
    val nr1:Int = 2
    val nr2:Int = 3

    println("Nr1 is 2")
    println("Nr2 is 3")

    println("+= "+(nr1 + nr2))
    println("-= "+(nr1 - nr2))
    println("*= "+(nr1 * nr2))
    println("/= "+(nr1 / nr2))
    println("%= "+(nr1 % nr2))

    println("3+4/2 = " + (3+4/2))
    println("(3+4)/2 = " + ((3+4)/2))
}